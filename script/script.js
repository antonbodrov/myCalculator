//Function Multiplizieren
function multiple() {
  let resultSave = document.form.textview.value; //Das Wert vom Input wird in das Variable gespeichert.
  let historySave = document.getElementById("history-value").innerText; //Das Wert vom History wird in das Variable gespeichert.
  let booleanTrueResult = resultSave.startsWith("-"); //Boolean Wert wird in das Variable gespeichert. Es wird TRUE wenn String vom Input Feld mit "-" anfängt.
  let booleanTrueHistory = historySave.endsWith("-"); //Boolean Wert wird in das Variable gespeichert. Es wird TRUE wenn String vom History Feld mit "-" endet.
  let booleanTrueHistoryPlus = historySave.endsWith("+"); //Boolean Wert wird in das Variable gespeichert. Es wird TRUE wenn String vom History Feld mit "+" endet.
  let booleanTrueHistoryMultiple = historySave.endsWith("*"); //Boolean Wert wird in das Variable gespeichert. Es wird TRUE wenn String vom History Feld mit "*" endet.
  let booleanTrueHistoryDivide = historySave.endsWith("/"); //Boolean Wert wird in das Variable gespeichert. Es wird TRUE wenn String vom History Feld mit "/" endet.
  let booleanTruePunkt = resultSave.endsWith("."); //Boolean Wert wird in das Variable gespeichert. Es wird TRUE wenn String vom History Feld mit "." endet.

  // Wenn beim History das letzte Zeichen = "/" und Input Feld nix hat, dann wird History in die Variable "backSave" gespeichert. Dann wirds letzte Zeichen entfernt und "*" am ende beim History hinzugefügt.
  if (historySave.substr(-1) == "/" && resultSave == "") {
    let backSave = document.getElementById("history-value").innerText;
    backSave = backSave.substring(0,backSave.length-1);
    document.getElementById("history-value").innerText = backSave + "*";
  }
  // Wenn beim History das letzte Zeichen = "*" und Input Feld nix hat, dann wird History in die Variable "backSave" gespeichert. Dann wirds letzte Zeichen entfernt und "*" am ende beim History hinzugefügt.
  else if (historySave.substr(-1) == "*" && resultSave == "") {
    let backSave = document.getElementById("history-value").innerText;
    backSave = backSave.substring(0,backSave.length-1);
    document.getElementById("history-value").innerText = backSave + "*";
  }
  // Wenn beim History das letzte Zeichen = "-" und Input Feld nix hat, dann wird History in die Variable "backSave" gespeichert. Dann wirds letzte Zeichen entfernt und "*" am ende beim History hinzugefügt.
  else if (historySave.substr(-1) == "-" && resultSave == "") {
    let backSave = document.getElementById("history-value").innerText;
    backSave = backSave.substring(0,backSave.length-1);
    document.getElementById("history-value").innerText = backSave + "*";
  }
  // Wenn beim History das letzte Zeichen = "+" und Input Feld nix hat, dann wird History in die Variable "backSave" gespeichert. Dann wirds letzte Zeichen entfernt und "*" am ende beim History hinzugefügt.
  else if (historySave.substr(-1) == "+" && resultSave == "") {
    let backSave = document.getElementById("history-value").innerText;
    backSave = backSave.substring(0,backSave.length-1);
    document.getElementById("history-value").innerText = backSave + "*";
  }
  // Wenn beim History nix hat und Input Feld nix hat, dann wird History in die Variable "backSave" gespeichert und es wird nix "" am ende beim History hinzugefügt.
  else if (historySave == "" && resultSave == "") {
    let backSave = document.getElementById("history-value").innerText;
    backSave = backSave.substring(0,backSave.length-1);
    document.getElementById("history-value").innerText = backSave + "";
  }
  // Wenn beim Input Feld die Zahl mit "-" anfängt und das Punkt am ende steht, dann wird Input in die Variable gespeichert, in Klammer gesetzt und 0 wird nach der punkt hinzugefügt. Dann wird es ganze zu History Feld hinzugefügt.
  else if(booleanTrueResult == true && booleanTruePunkt == true){
        let minusZahl = document.form.textview.value;
        minusZahlInKlammer = "(" + minusZahl + "0)";
        document.getElementById("history-value").innerText = document.getElementById("history-value").innerText + minusZahlInKlammer + "*";
        document.form.textview.value="";
  }
  // Wenn beim Input Feld die Zahl mit "-" anfängt und das "-" am ende im History steht, dann wird Input in die Variable gespeichert und in Klammer gesetzt. Dann wird es ganze zu History Feld hinzugefügt.
  else if(booleanTrueResult == true && booleanTrueHistory == true){
        let minusZahl = document.form.textview.value;
        minusZahlInKlammer = "(" + minusZahl + ")";
        document.getElementById("history-value").innerText = document.getElementById("history-value").innerText + minusZahlInKlammer + "*";
        document.form.textview.value="";
  }
  // Wenn beim Input Feld die Zahl mit "-" anfängt und das "/" im History am ende steht ODER
  //wenn beim Input Feld die Zahl mit "-" anfängt und das "*" im History am ende steht ODER
  // wenn beim Input Feld die Zahl mit "-" anfängt und das "+" im History am ende steht ODER
  // wenn beim Input Feld die Zahl mit "-" anfängt und das "-" im History am ende steht DANN
  //wird Input in die Variable gespeichert und in Klammer gesetzt. Dann wird es ganze zu History Feld hinzugefügt.
  else if(booleanTrueResult == true && booleanTrueHistoryDivide == true || booleanTrueResult == true && booleanTrueHistoryMultiple == true || booleanTrueResult == true && booleanTrueHistoryPlus == true || booleanTrueResult == true && booleanTrueHistory == true){
        let minusZahl = document.form.textview.value;
        minusZahlInKlammer = "(" + minusZahl + ")";
        document.getElementById("history-value").innerText = document.getElementById("history-value").innerText + minusZahlInKlammer + "*";
        document.form.textview.value="";
  }
  // Wenn beim Input Feld die Zahl mit "." endet, dann wird Input in die Variable gespeichert und 0 wird hinzugefügt. Dann wird es ganze zu History Feld hinzugefügt.
  else if(booleanTruePunkt == true){
        let zahl = document.form.textview.value;
        zahl = document.form.textview.value + "0";
        document.getElementById("history-value").innerText = document.getElementById("history-value").innerText + zahl + "*";
        document.form.textview.value="";
  }
  // Wenn es ein Fehler auftritt beim geteildurch 0, dann wenn man "*" anklickt wird alles von die Felder gelöscht.
  else if (resultSave == " Hmmmm! "+ String.fromCodePoint(0x1F635)){
    document.form.textview.value = "0";
    document.getElementById("history-value").innerText = "";
  }
  // Wenn es ein Fehler auftritt beim geteildurch 0, dann wenn man "*" anklickt wird alles von die Felder gelöscht.
  else if (resultSave == " Krass! "+ String.fromCodePoint(0x1F621)){
    document.form.textview.value = "0";
    document.getElementById("history-value").innerText = "";
  }
  // Falls es nix zutrift, dann wirds in die History alles gespeichert.
  else{
    document.getElementById("history-value").innerText = document.getElementById("history-value").innerText + document.form.textview.value + "*";
    document.form.textview.value="";
  }
}

